# Strings

# Subscripting
print("Hello"[0]) # H
print("Hello"[-1]) # o

# Concatenate
print("123" + "345") # 123345

# Integer = Whole numbers
print(123 + 345) # 468

# Large Integers - underscores helps us humans to read them in a better way
print(123_345_567) # 123345567

# Float = Decimal
print(3.14159)

# Boolean = True or False
print(True)
print(False)